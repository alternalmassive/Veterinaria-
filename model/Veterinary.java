package model;
import java.util.ArrayList;
public class Veterinary
{
//Constants------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public final static int QUANTITY_ROOMS = 8;
//Atributes------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

//Relations------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	private ArrayList<Human>humans;
	private ArrayList<Pet>records;
	private Room[]rooms;
//Constructor----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public Veterinary()
	{
		humans = new ArrayList<>();
		records = new ArrayList<>();
		rooms = new Room[QUANTITY_ROOMS];
	}
//Methods--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public Room[] getRooms()
	{
		return rooms;
	}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	
	public boolean checkRoomAvailability()
	{
		boolean availability = false;
		
		for(int i=0; i<QUANTITY_ROOMS && !availability; i++)
		{
			if(rooms[i]==null)
			{
				availability = true;
			}
		}
		return availability;
	}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	
	public void addHuman(Human create)
	{	
		humans.add(create);
	}
//---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	
	public void checkHuman()
	{
		int report;
		report = humans.size();
		System.out.println(report);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
}